export function buildDepartmentTree(elements, parentId = "") {
  let branch = [];
    elements.filter(element => element.parent === parentId).forEach(element => {
        let children = buildDepartmentTree(elements, element.id);
        if (children.length) {
            element.children = children;
        } else {
            element.children = [];
        }
        branch.push(element);
    });
    return branch;
}

